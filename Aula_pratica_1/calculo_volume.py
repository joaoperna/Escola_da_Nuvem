"""(doc strings)
Crie um programa que calcula o volume de uma caixa retangular. Use as seguintes dimensões:
Comprimento: 12 cm
Largura: 14 cm
Altura: 20 cm
O programa deve calcular o volume e exibir o resultado em cm³.
"""

# declarando variáveis
comprimento = 12
largura = 14
altura = 20

# calculando o volume corretamente
volume = comprimento * largura * altura

# exibindo o resultado
print(f"O volume da caixa é {volume} cm³")  # f strings
